package me.bird.heroku.feisu;

public class BookInfo {

	private String bookName;
	
	private String bookUrl;
	
	private String author;
	
	private String category;
	
	private String brief;
	
	private String lastestChapter;
	
	private String lastestChapterUrl;
	
	private String lastUpdate;
	
	private String bookImageUrl;
	
	private String contentUrl;

	public String getBookName() {
		return bookName;
	}

	public void setBookName(String bookName) {
		this.bookName = bookName;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getCategory() {
		return category;
	}

	public void setCategory(String category) {
		this.category = category;
	}

	public String getBrief() {
		return brief;
	}

	public void setBrief(String brief) {
		this.brief = brief;
	}

	public String getLastestChapter() {
		return lastestChapter;
	}

	public void setLastestChapter(String lastestChapter) {
		this.lastestChapter = lastestChapter;
	}

	public String getLastestChapterUrl() {
		return lastestChapterUrl;
	}

	public void setLastestChapterUrl(String lastestChapterUrl) {
		this.lastestChapterUrl = lastestChapterUrl;
	}

	public String getLastUpdate() {
		return lastUpdate;
	}

	public void setLastUpdate(String lastUpdate) {
		this.lastUpdate = lastUpdate;
	}

	public String getBookImageUrl() {
		return bookImageUrl;
	}

	public void setBookImageUrl(String bookImageUrl) {
		this.bookImageUrl = bookImageUrl;
	}

	public String getBookUrl() {
		return bookUrl;
	}

	public void setBookUrl(String bookUrl) {
		this.bookUrl = bookUrl;
	}

	public String getContentUrl() {
		return contentUrl;
	}

	public void setContentUrl(String contentUrl) {
		this.contentUrl = contentUrl;
	}
	
}
